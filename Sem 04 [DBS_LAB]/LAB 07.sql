FROM EMPLOYEES e
JOIN JOB_HISTORY j ON e.EMPLOYEE_ID = j.EMPLOYEE_ID;

SELECT EMPLOYEE_ID FROM emp_history
GROUP BY EMPLOYEE_ID
HAVING COUNT(DISTINCT DEPARTMENT_ID) > 1;

