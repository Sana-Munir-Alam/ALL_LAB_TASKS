---- Q31
CREATE VIEW emp_mgr_dept AS
SELECT e.FIRST_NAME emp, m.FIRST_NAME mgr, e.DEPARTMENT_ID e_dept, m.DEPARTMENT_ID m_dept
FROM EMPLOYEES e
JOIN EMPLOYEES m ON e.MANAGER_ID = m.EMPLOYEE_ID;

