       e2.FIRST_NAME || ' ' || e2.LAST_NAME AS Emp2,
       e1.DEPARTMENT_ID
FROM EMPLOYEES e1
JOIN EMPLOYEES e2 ON e1.DEPARTMENT_ID = e2.DEPARTMENT_ID AND e1.EMPLOYEE_ID < e2.EMPLOYEE_ID
WHERE e1.DEPARTMENT_ID IS NOT NULL;

