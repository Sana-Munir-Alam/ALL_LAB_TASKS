---- Q37
CREATE VIEW emp_city_mgr AS
SELECT e.EMPLOYEE_ID, l.CITY, m.EMPLOYEE_ID mgr_id, l2.CITY mgr_city FROM EMPLOYEES e
JOIN DEPARTMENTS d ON e.DEPARTMENT_ID = d.DEPARTMENT_ID
JOIN LOCATIONS l ON d.LOCATION_ID = l.LOCATION_ID
JOIN EMPLOYEES m ON e.MANAGER_ID = m.EMPLOYEE_ID
JOIN DEPARTMENTS d2 ON m.DEPARTMENT_ID = d2.DEPARTMENT_ID
JOIN LOCATIONS l2 ON d2.LOCATION_ID = l2.LOCATION_ID;

SELECT * FROM emp_city_mgr
WHERE CITY = mgr_city;

