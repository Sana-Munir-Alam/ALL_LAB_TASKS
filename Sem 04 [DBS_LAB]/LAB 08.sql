CREATE OR REPLACE TRIGGER trg_job_promotion_bonus BEFORE UPDATE OF JOB_ID ON EMPLOYEES 
FOR EACH ROW
DECLARE
    v_old_level NUMBER;
    v_new_level NUMBER;
BEGIN
    ---- Get job levels, default 0 if not found 
    SELECT NVL(JOB_LEVEL, 0) INTO v_old_level FROM JOB HIERARCHY 
    WHERE JOB_ID = OLD. JOB_ID;
    
    SELECT NVL (JOB_LEVEL, 0) INTO v_new_level FROM JOB HIERARCHY 
    WHERE JOB_ID = NEW.JOB_ID;

    -- if promoted to higher level, apply 5% bonus directly to :NEW.SALARY 
    IF v new level > v_old_level THEN
        :NEW.SALARY := NEW.SALARY* 1.05; 
        DBMS_OUTPUT.PUT_LINE('5% bonus applied to Employee' || NEW.EMPLOYEE_ID || ' for promotion from ' || :OLD.JOB_ID || ' to ' || :NEW.JOB_ID);
    END IF;
END;
