CREATE OR REPLACE TRIGGER trg_manager_level BEFORE INSERT ON EMPLOYEES 
FOR EACH ROW
BEGIN
    IF NEW.MANAGER_ID IS NOT NULL THEN
        DECLARE
            v_job VARCHAR2 (20);
        BEGIN
            ----Get manager's job
            SELECT JOB_ID INTO v_job FROM EMPLOYEES
            WHERE EMPLOYEE_ID = NEW.MANAGER_ID;
            ---- Assign MANAGER_LEVEL based on manager's job
            IF v_job = 'AD_PRES' THEN
                :NEW.MANAGER_LEVEL = 5;
            ELSIF v_job = 'AD_VP' THEN
                :NEW.MANAGER_LEVEL := 4;
            ELSIF v_job IN ('AD ASST', 'FI_MGR', 'AC_MGR', 'MK_MAN') THEN 
                :NEW.MANAGER_LEVEL := 3;
            ELSIF v job IN ('ST MAN', 'PU_MAN', 'SA_MAN') THEN
                :NEW.MANAGER_LEVEL = 2;
            ELSE
                :NEW.MANAGER_LEVEL := 1; all other employees
            END IF;
        END;
    ELSE
        :NEW.MANAGER_LEVEL := 0; ---- No manager assigned
    END IF;
END;
/


