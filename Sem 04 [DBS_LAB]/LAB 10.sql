DECLARE
    v_emp_id Employees.Employee_ID%TYPE := &Employee_ID;
    v_salary Employees.Salary%TYPE;
    v_dept_id Employees.Department_ID%TYPE;
    v_salary_cat VARCHAR2(30);
    v_dept_class VARCHAR2(30);
BEGIN
    SELECT Salary, Department_ID INTO v_salary, v_dept_id FROM EMPLOYEES
    WHERE Employee_ID = v_emp_id;

    v_salary_cat :=
        CASE
            WHEN v_salary < 5000 THEN 'LOW'
            WHEN v_salary BETWEEN 5000 AND 10000 THEN 'MEDIUM'
            ELSE 'HIGH'
        END;

    v_dept_class :=
        CASE
            WHEN v_dept_id IN (10, 20) THEN 'ADMIN/IT CORE'
            WHEN v_dept_id IN (30, 50) THEN 'SALES/OPS'
            ELSE 'OTHER'
        END;

    DBMS_OUTPUT.PUT_LINE('Employee ID: ' || v_emp_id);
    DBMS_OUTPUT.PUT_LINE('Salary Category: ' || v_salary_cat);
    DBMS_OUTPUT.PUT_LINE('Department Classification: ' || v_dept_class);
    DBMS_OUTPUT.PUT_LINE('Status: ' || v_salary_cat || ' - ' || v_dept_class);
END;
