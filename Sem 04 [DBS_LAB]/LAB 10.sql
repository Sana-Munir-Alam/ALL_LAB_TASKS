CREATE OR REPLACE PROCEDURE JOB_EMP_REPORT (P_JOB_ID VARCHAR2) AS
BEGIN
    FOR REC IN (SELECT E.EMPLOYEE_ID, E.FIRST_NAME, D.DEPARTMENT_NAME, E.SALARY, E.HIRE_DATE FROM EMPLOYEES E
                JOIN DEPARTMENTS D ON E.DEPARTMENT_ID = D.DEPARTMENT_ID
                WHERE E.JOB_ID = P_JOB_ID) LOOP

        DBMS_OUTPUT.PUT_LINE(REC.EMPLOYEE_ID || ' | ' || REC.FIRST_NAME || ' | ' ||
                             REC.DEPARTMENT_NAME || ' | ' || REC.SALARY || ' | ' || REC.HIRE_DATE);
    END LOOP;
END;
/

