SET SERVEROUTPUT ON;
DECLARE
    V_AVG NUMBER;
    V_JOB_CLASS VARCHAR2(30);
    V_PRIORITY VARCHAR2(30);
BEGIN
    SELECT AVG(SALARY) INTO V_AVG FROM EMPLOYEES;

    FOR REC IN (SELECT EMPLOYEE_ID, JOB_ID, DEPARTMENT_ID, SALARY FROM EMPLOYEES) LOOP

        IF REC.SALARY < V_AVG THEN

            V_JOB_CLASS :=
                CASE
                    WHEN REC.JOB_ID LIKE '%MAN%' THEN 'MANAGER'
                    WHEN REC.JOB_ID LIKE '%DEV%' THEN 'DEVELOPER'
                    WHEN REC.JOB_ID LIKE '%SALES%' THEN 'SALES'
                    ELSE 'STAFF'
                END;

            V_PRIORITY :=
                CASE
                    WHEN REC.DEPARTMENT_ID IN (10, 20) THEN 'HIGH PRIORITY'
                    WHEN REC.DEPARTMENT_ID IN (30, 50) THEN 'MEDIUM PRIORITY'
                    ELSE 'LOW PRIORITY'
                END;
            DBMS_OUTPUT.PUT_LINE(REC.EMPLOYEE_ID || ' | ' || V_JOB_CLASS || ' | BELOW AVG | ' || V_PRIORITY);
        END IF;
    END LOOP;
END;
/


