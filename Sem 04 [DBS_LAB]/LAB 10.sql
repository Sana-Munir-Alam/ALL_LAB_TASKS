DECLARE
    V_NEW_SALARY NUMBER;
    V_CLASS VARCHAR2(30);
BEGIN
    FOR E IN (SELECT EMPLOYEE_ID, DEPARTMENT_ID, SALARY, HIRE_DATE FROM EMPLOYEES) LOOP

        V_NEW_SALARY := E.SALARY;

        IF E.DEPARTMENT_ID IN (10, 20) THEN
            V_NEW_SALARY := V_NEW_SALARY * 1.10;
        ELSIF E.DEPARTMENT_ID IN (30, 50) THEN
            V_NEW_SALARY := V_NEW_SALARY * 1.05;
        END IF;

        IF MONTHS_BETWEEN(SYSDATE, E.HIRE_DATE) > 60 THEN
            V_NEW_SALARY := V_NEW_SALARY * 1.05;
        END IF;

        V_CLASS :=
            CASE
                WHEN V_NEW_SALARY < 5000 THEN 'LOW'
                WHEN V_NEW_SALARY BETWEEN 5000 AND 10000 THEN 'MEDIUM'
                ELSE 'HIGH'
            END;
        DBMS_OUTPUT.PUT_LINE(E.EMPLOYEE_ID || ' | OLD: ' || E.SALARY || ' | NEW: ' || V_NEW_SALARY || ' | CLASS: ' || V_CLASS);
    END LOOP;
END;
