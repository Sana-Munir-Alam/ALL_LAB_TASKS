----Q17
SET SERVEROUTPUT ON;
CREATE OR REPLACE PROCEDURE RAISE_SALARY ( P_EMP_ID NUMBER, P_PERCENT NUMBER) AS
    V_NAME EMPLOYEES.FIRST_NAME%TYPE;
    V_SAL  EMPLOYEES.SALARY%TYPE;
BEGIN
    SELECT FIRST_NAME, SALARY INTO V_NAME, V_SAL FROM EMPLOYEES
    WHERE EMPLOYEE_ID = P_EMP_ID;

    V_SAL := V_SAL + (V_SAL * P_PERCENT / 100);

    UPDATE EMPLOYEES
    SET SALARY = V_SAL
    WHERE EMPLOYEE_ID = P_EMP_ID;

    DBMS_OUTPUT.PUT_LINE('EMPLOYEE NAME: ' || V_NAME);
    DBMS_OUTPUT.PUT_LINE('UPDATED SALARY: ' || V_SAL);
EXCEPTION
    WHEN NO_DATA_FOUND THEN DBMS_OUTPUT.PUT_LINE('EMPLOYEE NOT FOUND');
END;
/

