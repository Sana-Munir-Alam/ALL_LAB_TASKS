DECLARE
    V_SALARY_LEVEL VARCHAR2(20);
    V_COMM NUMBER;
    V_BONUS VARCHAR2(30);
BEGIN
    FOR REC IN (SELECT EMPLOYEE_ID, DEPARTMENT_ID, SALARY, COMMISSION_PCT FROM EMPLOYEES) LOOP
        V_COMM := NVL(REC.COMMISSION_PCT, 0);
        V_SALARY_LEVEL :=
            CASE
                WHEN REC.SALARY < 5000 THEN 'LOW'
                WHEN REC.SALARY BETWEEN 5000 AND 10000 THEN 'MEDIUM'
                ELSE 'HIGH'
            END;

        IF REC.DEPARTMENT_ID IN (10, 20) AND V_SALARY_LEVEL = 'HIGH' AND V_COMM >= 0.2 THEN
            V_BONUS := 'ELITE BONUS';
        ELSIF REC.DEPARTMENT_ID IN (30, 50) AND V_SALARY_LEVEL IN ('MEDIUM','HIGH') AND V_COMM > 0 THEN
            V_BONUS := 'STANDARD BONUS';
        ELSIF V_COMM = 0 THEN
            V_BONUS := 'NO BONUS (NO COMMISSION)';
        ELSE
            V_BONUS := 'MINIMAL BONUS';
        END IF;
        DBMS_OUTPUT.PUT_LINE(REC.EMPLOYEE_ID || ' | DEPT ' || REC.DEPARTMENT_ID || ' | ' || V_SALARY_LEVEL || ' | COMM ' || V_COMM || ' => ' || V_BONUS);
    END LOOP;
END;
/


