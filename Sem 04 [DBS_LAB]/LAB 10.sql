DECLARE
    V_COMM NUMBER;
    V_STATUS VARCHAR2(30);
BEGIN
    FOR REC IN (SELECT EMPLOYEE_ID, SALARY, COMMISSION_PCT FROM EMPLOYEES) LOOP
        V_COMM := NVL(REC.COMMISSION_PCT, 0);

        IF REC.SALARY > 10000 AND V_COMM > 0.2 THEN
            V_STATUS := 'EXCELLENT';
        ELSIF REC.SALARY > 7000 THEN
            V_STATUS := 'GOOD';
        ELSE
            V_STATUS := 'AVERAGE';
        END IF;

        DBMS_OUTPUT.PUT_LINE(REC.EMPLOYEE_ID || ' => ' || V_STATUS);
    END LOOP;
END;
