DECLARE
    V_TOTAL NUMBER;
    V_AVG NUMBER;
    V_BUDGET VARCHAR2(20);
BEGIN
    FOR D IN (SELECT DEPARTMENT_ID FROM DEPARTMENTS) LOOP
        SELECT NVL(SUM(SALARY),0), NVL(AVG(SALARY),0)
        INTO V_TOTAL, V_AVG
        FROM EMPLOYEES
        WHERE DEPARTMENT_ID = D.DEPARTMENT_ID;

        V_BUDGET :=
            CASE
                WHEN V_TOTAL > 50000 THEN 'HIGH BUDGET'
                WHEN V_TOTAL BETWEEN 20000 AND 50000 THEN 'MEDIUM BUDGET'
                ELSE 'LOW BUDGET'
            END;
        DBMS_OUTPUT.PUT_LINE('DEPT ' || D.DEPARTMENT_ID || ' TOTAL: ' || V_TOTAL || ' AVG: ' || V_AVG || ' => ' || V_BUDGET);
    END LOOP;
END;
